package javaPrograms;

public class T7IfElseStatement {
	public static void main(String[] args)
	{
		int a=21; //Value should be non zero in if else statement
		
		if(a>0)
		{
			System.out.println(" A is positive number" +a);
		}
		else
		{
			System.out.println(" A is a negative number" +a);
		}
				

	}
}
